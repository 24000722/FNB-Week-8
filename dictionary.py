# dictionary is defined by curly braces and separate each value with a colon

my_dict = {'apple' :3, 'banana' :5, 'orange' :2}
#the fruits here are keys and numbers are the corresponding values

print(my_dict['apple']) # prints the value associated with apple which is 3

my_dict['grapes'] = 4  # adds grape and value 4 to the list
print(my_dict)

my_dict['banana'] = 7  # changes value of banana to 7
print(my_dict)
