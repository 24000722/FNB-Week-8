fruits = ["apple", "banana", "cherry"]

fruits.append("kiwi")  # adds kiwi to the list
print(fruits)

fruits.insert(1, "orange")   # adds orange in position 1
print(fruits)

fruits.remove("kiwi")    # removes kiwi from the list
print(fruits)

fruits.sort()   # arranges the list in ascending order
print(fruits)

fruits.sort(reverse = True) # arranges the list in descending order
print(fruits)